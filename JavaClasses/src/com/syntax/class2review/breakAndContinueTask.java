package com.syntax.class2review;

public class breakAndContinueTask {
	
	public static void main(String[] args) {
		// print number form 1-50 expect those that are divisible by 3 
		
		
		for (int i=1; i<=50; i++) {
				if ( i%3==0) {
					continue;
				
				} 
				System.out.println(i);
				
	}
		}
}
